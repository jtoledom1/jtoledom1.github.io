create database aerolinea;
use aerolinea;
create table Usuarios
(
id_Usuario int primary key,
Nombre varchar(30),
ApellidoP varchar(30),
ApellidoM varchar(30),
Correo varchar(30),
Pass varchar(30),
TDC varchar(30)
);
create table Ciudades
(
id_Ciudad int primary key,
Ciudad_destino varchar(30),
Ciudad_Partida varchar(30)
);

create table Horarios
(
id_Horario int primary key,
HoraPartida varchar(30),
HoraLlegada varchar(30),
Fecha date
);

create table Aviones
(
id_Avion int primary key,
Mod_Avion varchar(30)
);

create table Vuelos
(
Id_Vuelo int primary key,
id_Horario int, foreign key (id_Horario) references Horarios(id_Horario),
costo varchar(30),
Aerolinea varchar(30),
TipoVuelo varchar(30)
);

create table Detalle_CiudVuelos
(
Id_Detalle_CiudVuel int primary key,
Id_Ciudad int, foreign key (Id_Ciudad) references Ciudades(Id_Ciudad),
Id_Vuelo int, foreign key (Id_Vuelo) references Vuelos(Id_Vuelo)
);

create table compras
(
Id_Compra int primary key,
Id_Usuario int, foreign key (Id_Usuario) references Usuarios(Id_Usuario),
Id_Vuelo int, foreign key (Id_Vuelo) references Vuelos(Id_Vuelo)
);

create table CategoriasAsientos
(
Id_CategoriaAsientos int primary key,
id_Avion int, foreign key (id_Avion) references Aviones(id_Avion),
Id_Vuelo int, foreign key (Id_Vuelo) references Vuelos(Id_Vuelo),
Categoria varchar(30),
CostoAd varchar(30),
AsientosDisponibles varchar(30)
);

